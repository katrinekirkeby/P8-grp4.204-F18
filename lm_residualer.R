lmrace <- lm(balle~EUR+AFR, data = hudcomplete)
res <- rstandard(lmrace)
par(mfrow=c(3,2), mar=c(4,4,2,2))
hist(res, xlab = 'Standardiserede residualer', main='Histogram af residualer', ylab = 'Frekvens')
qqnorm(res, xlab = 'Teoretiske fraktiler', ylab = 'Empiriske fraktiler')
qqline(res,col='red')
plot(res, ylab = 'Standardiserede residualer',xlab='Indeks',main = 'Plot af residualer')
plot(predict(lmrace),res, xlab = 'Fittede værdier', ylab='Standardiserede residualer',main = 'Residualer mod fittede værdier')
plot(hudcomplete$EUR,res,xlab = 'Andel europæisk afstamning', ylab = 'Standardiserede residualer', main = 'Residualer mod europæisk afstamning')
plot(hudcomplete$AFR,res,xlab = 'Andel afrikansk afstamning', ylab = 'Standardiserede residualer', main = 'Residualer mod afrikansk afstamning')




